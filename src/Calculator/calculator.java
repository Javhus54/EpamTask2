package Calculator;

public class calculator {
	public int additionOfTwoNumbers(int variable1,int variable2)
	{
		return variable1 + variable2;
	}
	public int subtractionOfTwoNumbers(int variable1,int variable2)
	{
		return variable1 + variable2;
	}
	public int multiplicationOfTwoNumbers(int variable1,int variable2)
	{
		return variable1 * variable2;
	}
	public double DivisionOfTwoNumbers(int variable1,int variable2)
	{
		return variable1 / variable2;
	}
}
